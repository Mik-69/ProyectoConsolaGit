public class Operaciones {
    public static void main(String[] args) {
        
        int num1 = 10;
        int num2 = 5;
        int resultado1, resultado2;

        resultado1 = num1 * num2;
        resultado2 = num1 / num2;

        System.out.println("La multiplicación de " + num1 + " x " + num2 + " es: " + resultado1);
        System.out.println("La división de " + num1 + " / " + num2 + " es: " + resultado2);

    }
}